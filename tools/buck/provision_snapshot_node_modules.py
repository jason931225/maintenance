#!/usr/bin/env python3
"""Link a verified lockfile-consistent Node dependency tree into a snapshot."""
from __future__ import annotations

import argparse
import hashlib
import json
import os
import sys
from pathlib import Path


class ProvisionError(ValueError):
    pass


def digest(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def fail(message: str) -> None:
    raise ProvisionError("snapshot-node-deps: " + message)


def locked_packages(lock: dict) -> dict[str, dict]:
    packages = lock.get("packages")
    if not isinstance(packages, dict):
        fail("package-lock packages map is required")
    result: dict[str, dict] = {}
    for path, entry in packages.items():
        if not path.startswith("node_modules/"):
            continue
        if not isinstance(entry, dict):
            fail(f"package-lock entry is invalid: {path}")
        if entry.get("link") is True:
            if not isinstance(entry.get("resolved"), str):
                fail(f"linked package has no resolved path: {path}")
        elif not isinstance(entry.get("version"), str):
            fail(f"package-lock lacks a resolved package version for {path}")
        result[path] = entry
    return result


def provision(caller: Path, snapshot: Path) -> Path:
    for filename in ("package.json", "package-lock.json"):
        source, candidate = caller / filename, snapshot / filename
        if not source.is_file() or not candidate.is_file():
            fail(f"{filename} must exist in caller and immutable snapshot")
        if digest(source) != digest(candidate):
            fail(f"{filename} digest differs from immutable snapshot")
    try:
        lock = json.loads((caller / "package-lock.json").read_text(encoding="utf-8"))
    except json.JSONDecodeError as error:
        fail(f"invalid package-lock.json: {error}")
    modules = caller / "node_modules"
    if not modules.is_dir():
        fail("lockfile-installed node_modules is absent; run npm ci in the caller before preflight")
    for relative, expected in locked_packages(lock).items():
        installed = caller / relative
        if not installed.exists():
            fail(f"installed package is absent: {relative}")
        if expected.get("link") is True:
            target = (caller / expected["resolved"]).resolve()
            if not installed.is_symlink() or installed.resolve() != target:
                fail(f"installed linked package differs from lockfile: {relative}")
            continue
        manifest = installed / "package.json"
        if not manifest.is_file():
            fail(f"installed package manifest is absent: {relative}")
        try:
            actual = json.loads(manifest.read_text(encoding="utf-8")).get("version")
        except json.JSONDecodeError as error:
            fail(f"invalid installed package manifest for {relative}: {error}")
        if actual != expected["version"]:
            fail(f"installed package version differs from lockfile: {relative} ({actual!r} != {expected['version']!r})")
    destination = snapshot / "node_modules"
    if destination.exists() or destination.is_symlink():
        fail("snapshot already has node_modules; archive must not contain mutable dependencies")
    # This is a reference, never a copy: generators may read exact lockfile
    # dependencies but cannot treat a parent directory as an ambient resolver.
    os.symlink(modules, destination, target_is_directory=True)
    return destination


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--caller", required=True, type=Path)
    parser.add_argument("--snapshot", required=True, type=Path)
    args = parser.parse_args()
    try:
        destination = provision(args.caller.resolve(), args.snapshot.resolve())
    except (OSError, ProvisionError) as error:
        print(error, file=sys.stderr)
        return 1
    print(f"snapshot-node-deps: linked verified dependencies at {destination}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
