#!/usr/bin/env python3
"""Regression locks for hermetic snapshot Node dependency provisioning."""

import importlib.util
import json
import shutil
import tempfile
import unittest
from pathlib import Path

ROOT = Path(__file__).parent
SPEC = importlib.util.spec_from_file_location("provision_snapshot_node_modules", ROOT / "provision_snapshot_node_modules.py")
assert SPEC and SPEC.loader
PROVISION = importlib.util.module_from_spec(SPEC)
SPEC.loader.exec_module(PROVISION)


class SnapshotNodeModulesTests(unittest.TestCase):
    def make_tree(self) -> tuple[Path, Path, Path]:
        temp = Path(tempfile.mkdtemp(prefix="snapshot-node-deps-"))
        caller, snapshot = temp / "caller", temp / "snapshot"
        lock = {
            "lockfileVersion": 3,
            "packages": {
                "": {"devDependencies": {"openapi-typescript": "7.13.0"}},
                "node_modules/openapi-typescript": {"version": "7.13.0"},
            },
        }
        for root in (caller, snapshot):
            root.mkdir()
            (root / "package.json").write_text('{"devDependencies":{"openapi-typescript":"7.13.0"}}\n')
            (root / "package-lock.json").write_text(json.dumps(lock))
        manifest = caller / "node_modules/openapi-typescript/package.json"
        manifest.parent.mkdir(parents=True)
        manifest.write_text('{"name":"openapi-typescript","version":"7.13.0"}\n')
        return temp, caller, snapshot

    def test_links_verified_dependencies_without_copying_or_ancestor_resolution(self) -> None:
        temp, caller, snapshot = self.make_tree()
        try:
            destination = PROVISION.provision(caller, snapshot)
            self.assertTrue(destination.is_symlink())
            self.assertEqual((caller / "node_modules").resolve(), destination.resolve())
            self.assertTrue((snapshot / "node_modules/openapi-typescript/package.json").is_file())
        finally:
            shutil.rmtree(temp)

    def test_missing_or_lock_inconsistent_dependencies_fail_closed(self) -> None:
        temp, caller, snapshot = self.make_tree()
        try:
            shutil.rmtree(caller / "node_modules")
            with self.assertRaisesRegex(PROVISION.ProvisionError, "node_modules is absent"):
                PROVISION.provision(caller, snapshot)
        finally:
            shutil.rmtree(temp)
        temp, caller, snapshot = self.make_tree()
        try:
            (caller / "node_modules/openapi-typescript/package.json").write_text('{"version":"0.0.0"}\n')
            with self.assertRaisesRegex(PROVISION.ProvisionError, "differs from lockfile"):
                PROVISION.provision(caller, snapshot)
        finally:
            shutil.rmtree(temp)

    def add_locked_package(self, caller: Path, snapshot: Path, name: str, entry: dict, installed_version: str | None) -> None:
        for root in (caller, snapshot):
            lock = json.loads((root / "package-lock.json").read_text())
            lock["packages"]["node_modules/" + name] = entry
            (root / "package-lock.json").write_text(json.dumps(lock))
        if installed_version is not None:
            manifest = caller / "node_modules" / name / "package.json"
            manifest.parent.mkdir(parents=True, exist_ok=True)
            manifest.write_text(json.dumps({"name": name, "version": installed_version}))

    def test_absent_optional_or_platform_incompatible_packages_are_permitted(self) -> None:
        temp, caller, snapshot = self.make_tree()
        try:
            self.add_locked_package(caller, snapshot, "optional", {"version": "1.0.0", "optional": True}, None)
            self.add_locked_package(caller, snapshot, "linux-only", {"version": "1.0.0", "os": ["linux"]}, None)
            PROVISION.provision(caller, snapshot, platform="darwin", arch="arm64")
        finally:
            shutil.rmtree(temp)

    def test_required_absence_and_installed_optional_mismatch_fail_closed(self) -> None:
        temp, caller, snapshot = self.make_tree()
        try:
            self.add_locked_package(caller, snapshot, "required", {"version": "1.0.0"}, None)
            with self.assertRaisesRegex(PROVISION.ProvisionError, "installed package is absent"):
                PROVISION.provision(caller, snapshot)
        finally:
            shutil.rmtree(temp)
        temp, caller, snapshot = self.make_tree()
        try:
            self.add_locked_package(caller, snapshot, "optional", {"version": "1.0.0", "optional": True}, "0.0.0")
            with self.assertRaisesRegex(PROVISION.ProvisionError, "differs from lockfile"):
                PROVISION.provision(caller, snapshot)
        finally:
            shutil.rmtree(temp)

    def test_candidate_digest_mismatch_fails_before_linking(self) -> None:
        temp, caller, snapshot = self.make_tree()
        try:
            (snapshot / "package-lock.json").write_text('{}\n')
            with self.assertRaisesRegex(PROVISION.ProvisionError, "digest differs"):
                PROVISION.provision(caller, snapshot)
            self.assertFalse((snapshot / "node_modules").exists())
        finally:
            shutil.rmtree(temp)


if __name__ == "__main__":
    unittest.main()
